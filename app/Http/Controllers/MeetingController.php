     */
    public function store(Request $request)
    {
        $userId = auth()->user()->id;

        $this->validate($request, [
            'meeting_date'=>'required',
            'meeting_place'=>'required',
            'id_board'=>'required'
        ]);

        $boardId=$request->get('id_board');

        $meeting=Meeting::create([
            'date'=>$request->get('meeting_date'),
            'place'=>$request->get('meeting_place'),
            'name'=>$request->get('meeting_name'),
            'id_board'=>$boardId
        ]);

        return redirect($boardId.'/tasks')->with('success', 'Meeting added!');
    }

